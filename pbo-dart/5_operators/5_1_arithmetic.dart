void main() {
  int x = 1;
  int y = 2;
  int penjumlahan = x + y;
  int pengurangan = y - x;

  print("Hasil dari penjumlahan $x dan $y = " + penjumlahan.toString());
  print("Hasil dari pengurangan $y dan $x adalah $pengurangan");
}

// Output:
// Hasil dari penjumlahan 1 dan 2 = 3
// Hasil dari pengurangan 2 dan 1 adalah 1